no = int(input("Enter the digit: "))
if no == 0:
    print("Zero")
elif no == 1:
    print("One")
elif no == 2:
    print("Two")
elif no == 3:
    print("Three")
elif no == 4:
    print("Four")
elif no == 5:
    print("Five")
elif no == 6:
    print("Six")
elif no == 7:
    print("Seven")
elif no == 8:
    print("Eight")
else:
    print("Nine")
